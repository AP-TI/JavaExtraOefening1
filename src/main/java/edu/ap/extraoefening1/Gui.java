    private void updateLijst() {
        DefaultListModel defaultListModel = new DefaultListModel();
        for (Bestand bestand : collectie.getMuziek()) {
            defaultListModel.addElement(bestand);
        }
        jListBestanden.setModel(defaultListModel);
    }
    
    private void setVisibility(String type) {
        if ("film".equals(type)) {
            jPanelSpeelduur.setVisible(true);
            jPanelMuziek.setVisible(false);
            jPanelFoto.setVisible(false);
        } else if ("muziek".equals(type)) {
            jPanelSpeelduur.setVisible(true);
            jPanelMuziek.setVisible(true);
            jPanelFoto.setVisible(false);
        } else {
            jPanelSpeelduur.setVisible(false);
            jPanelMuziek.setVisible(false);
            jPanelFoto.setVisible(true);
        }
    }

