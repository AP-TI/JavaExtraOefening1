    private void jButtonToevoegenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonToevoegenActionPerformed
        Bestand bestand;
        try {
            if (jRadioButtonFoto.isSelected()) {
                
                bestand = new Foto(jTextFieldNaam.getText(), Double.parseDouble(jTextFieldGrootte.getText()), Integer.parseInt(jTextFieldPixelsPerInch.getText()));
                
            } else if (jRadioButtonMuziek.isSelected()) {
                if(jCheckBoxHeeftGenre.isSelected())
                    bestand = new Muziek(jTextFieldNaam.getText(), Double.parseDouble(jTextFieldGrootte.getText()), Duration.ofMinutes(Integer.parseInt(jTextFieldSpeelduur.getText())), jTextFieldNaamArtiest.getText(), Genre.findGenreByBeschrijving(jComboBoxGenre.getSelectedItem().toString()));
                else
                    bestand = new Muziek(jTextFieldNaam.getText(), Double.parseDouble(jTextFieldGrootte.getText()), Duration.ofMinutes(Integer.parseInt(jTextFieldSpeelduur.getText())), jTextFieldNaamArtiest.getText());
            } else {
                bestand = new Film(jTextFieldNaam.getText(), Double.parseDouble(jTextFieldGrootte.getText()), Duration.ofMinutes(Integer.parseInt(jTextFieldSpeelduur.getText())));
            }
            collectie.addBestand(bestand);
            updateLijst();
        } catch (java.lang.NumberFormatException ex) {
            JOptionPane.showConfirmDialog(this, "Geef een getal in", "Fout!", 2);
        }

    }//GEN-LAST:event_jButtonToevoegenActionPerformed

